                    <p id="carrera">La unidad de Extensión Universitaria se crea con el objeto de fortalecer la presencia 
                    de la universidad en la comunidad con actividades, complementarias a su función de 
                    formación de profesionales, de servicio y cooperación con empresas e instituciones 
                    públicas y privadas.  Las actividades de esta unidad se desarrollan con el concurso de 
                    docentes de tiempo completo y tiempo horario, fortaleciendo su contribución al 
                    compromiso de excelencia académica de la universidad.
                    </p>
                    <p id="carrera">
                    Desde su creación en el año de 2005, Extensión Universitaria ofreció cursos de 
                    formación y actualización profesional y empresarial para más de cien empresas e 
                    instituciones privadas y públicas, llegando a capacitar hasta la fecha, a más de 4.000 
                    personas en las áreas de gestión empresarial, marketing, recursos humanos, liderazgo, 
                    trabajo en equipo, habilidades directivas, medioambiente, calidad, turismo, protocolo, 
                    emprendimiento,  computación, diseño gráfico, fotografía, habilidades personales, 
                    seguridad e higiene industrial, petróleo y gas natural y legislación.</p>
                    </p>

                    <p id="carrera">La oferta académica de extensión Universitaria comprende programas y cursos con 
                    diferentes objetivos y periodos de duración:
                    </p>
                    <p id="carrera"><i class="b">1</i>. Cursos abiertos de formación contínua: De 15 a 21 hrs. académicas de duración. 
                    Los participantes encuentran temas de actualización en áreas técnicas y 
                    profesionales. Los cursos son dinámicos con un enfoque cien por ciento práctico. 
                    </p>
                    <p id="carrera"><i class="b">2</i>. Cursos para la formación de expertos: De una duración de entre  48 a 110 hrs. 
                    académicas. Los participantes adquieren un nivel de experticia en diferentes áreas 
                    del conocimiento humano.
                    </p>
                    <p id="carrera"><i class="b">3</i>. Cursos Cerrados: Dirigidos a empresas e instituciones públicas y privadas, y 
                    diseñados de acuerdo a sus requerimientos específicos.
                    </p>
                    <p id="carrera"><i class="b">4</i>. Programas On-line: Cursos y programas diseñados para empresas o instituciones 
                    impartidos de manera semipresencial y a distancia. 
                    </p>
                    <p id="carrera"><i class="b">5</i>. Diplomados: De una duración de 200 a 240 hrs. académicas, dirigidos al personal 
                    técnico y profesional que requiera la formación especializada en áreas empresariales 
                    y técnicas.
                    </p> 
